
public class Arithematic {
	public int add(int num1,int num2)
	{
		return num1+num2;
	}
    public int sub(int num1,int num2)
    {
    	return num1-num2;
    }
    public int mul(int num1, int num2)
    {
    	return num2*num1;
    }
    public float div(float num1,float num2)
    {
    	return num1/num2;
    }
    public int remainder(int num1,int num2)
    {
    	return num2%num1;
    	
    }
}
